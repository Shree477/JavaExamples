
public class ArithmaticDemo {

	public static void main(String[] args) {
		
		int x=10;
		int y=5;
		int z;
		
		z=x+y;           
		System.out.println(z);//Addition
		
		z=x-y;
		System.out.println(z);//Subtraction
		
		z=x*y;
		System.out.println(z);//Multiplication
		
		z=x/y;
		System.out.println(z);//Division
		
		z=x%y;
		System.out.println(z);//MOD Reminder

	}

}
