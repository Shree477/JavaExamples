
public class Star1 {

	public static void main(String[] args) {
		for(int i=1; i<=6; i++) //7
			//0 1 2 3 4 5
		{   
		//inner loop for columns  
			for(int j=1; j<=i; j++)  //j=1 2 3 4 5 6
			{   
				//prints stars   
				System.out.print("* ");   
			}   
		  //throws the cursor in a new line after printing each line  
		 System.out.println();   
		}

}


	}
