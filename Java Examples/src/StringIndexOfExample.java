
public class StringIndexOfExample {

	public static void main(String[] args) {
			
		
		String str1="Himashu";
		
		int pos=str1.indexOf('s');
		
		System.out.println(pos);
		
		
		String str2="AaBddddddCDa";			// 2nd Index
		
		int pos2=str2.indexOf('a',2);
		
		
		System.out.println(pos2);

		
	}

}
