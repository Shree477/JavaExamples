
public class TwoDArray {

	public static void main(String[] args) {
		
		int table[][]=new int[3][2];   			// Creating array	
		
			table[0][0]=11;						//Initializing array
			table[0][1]=12;
			table[1][0]=13;
			table[1][1]=14;
			table[2][0]=15;
			table[2][1]=16;	
			
			
			
			System.out.println(table[2][0]);
	}
	
	
}
